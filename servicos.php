                <?php
                $dir = 'img/gallery/servicos/';
                $imagens = [];
                $videos = [];

                if (is_dir($dir)) {
                    $files = scandir($dir);

                    foreach ($files as $file) {
                        $path = $dir . $file;
                        if (is_file($path)) {
                            $ext = strtolower(pathinfo($file, PATHINFO_EXTENSION));
                            if (in_array($ext, ['jpg', 'jpeg', 'png', 'gif', 'avif', 'heic'])) {
                                $imagens[] = $file;
                            } elseif (in_array($ext, ['mp4', 'webm', 'ogg'])) {
                                $videos[] = $file;
                            }
                        }
                    }

                    // Embaralhar e limitar a 5
                    shuffle($imagens);
                    shuffle($videos);
                    $imagens = array_slice($imagens, 0, 5);
                    $videos = array_slice($videos, 0, 5);
                }

                foreach ($imagens as $img) {
                    echo '
    <div class="col-md-4 col-sm-6 mb-4">
        <div class="gallery_card">
            <a href="' . $dir . $img . '" class="glightbox" data-gallery="galeria-servicos">
                <img src="' . $dir . $img . '" alt="">
                <div class="overlay">
                    <i class="fas fa-search-plus"></i>
                    <div class="title">Visualizar</div>
                </div>
            </a>
        </div>
    </div>';
                }


                foreach ($videos as $vid) {
                    echo '
    <div class="col-md-4 col-sm-6 mb-4">
        <div class="gallery_card">
            <a href="' . $dir . $vid . '" class="glightbox" data-gallery="galeria-servicos" data-type="video">
